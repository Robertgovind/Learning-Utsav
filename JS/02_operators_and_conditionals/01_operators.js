// Arithmetic Operators
let a = 5;
let b = 3;
console.log("Aritmetic operators :");
console.log("a + b = ", a+b);
console.log("a - b = ", a-b);
console.log("a * b = ", a*b);
console.log("a / b = ", a/b);
console.log("a % b = ", a%b);  // Modulus
console.log("a ** b = ", a**b); // Exponentiation

// Unary Operators
console.log("Unary operators");
console.log("++a = ", ++a);
console.log("--b = ",--b);

// Assignment operators
console.log("Assignment operators : ");
console.log("a += 2", a += 2);
console.log("a -= 2", a -= 2);
console.log("a *= 2", a *= 2);
console.log("a /= 2", a /= 2);
console.log("a %= 2", a %= 2);
console.log("a **= 3", a **= 3);

// Coparison operators
console.log("Comparison operators :");
console.log("a == b", a == b);
console.log("a != b",a != b);
console.log("a === b", a === b);  // isEqual and type same
console.log("'5' === '5'", '5' === '5');
console.log("a > b", a > b);
console.log("a >= b", a >= b);
console.log("a < b", a < b);
console.log("a <= b", a <= b);


// Logical operators
